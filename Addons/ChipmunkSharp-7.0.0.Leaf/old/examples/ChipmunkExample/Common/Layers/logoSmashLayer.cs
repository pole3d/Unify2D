		public cpBall make_ball(float x, float y)
		{
			var body = new cpBody(1, cp.Infinity);
			body.SetPosition(new cpVect(x, y));

			var shape = new cpBall(body, 0.95f, cpVect.Zero);
			//var shape = new cpBall(body, 35, cpVect.Zero);
			shape.SetElasticity(0);
			shape.SetFriction(0);

			return shape;
		}



		public override void OnEnter()
		{
			base.OnEnter();


			space.SetIterations(1);

			bodyCount = 0;

			cpBody body;
			cpBall shape;

			for (var y = 0; y < image_height; y++)
			{
				for (var x = 0; x < image_width; x++)
				{
					if (get_pixel(x, y) == 0)
						continue;

					float x_jitter = 0.05f * CCRandom.Float_0_1();
					float y_jitter = 0.05f * CCRandom.Float_0_1();

					shape = make_ball((2 * (x - image_width / 2 + x_jitter)), (2 * (image_height / 2 - y + y_jitter)));

					space.AddBody(shape.body);
					space.AddShape(shape);
					bodyCount++;
				}
			}

			body = space.AddBody(new cpBody(1e9f, cp.Infinity));
			body.SetPosition(new cpVect(-1000, 0 - 10));
			body.SetVelocity(new cpVect(400, 0));

			shape = space.AddShape(new cpBall(body, 8, cpVect.Zero)) as cpBall;
			shape.SetElasticity(0);
			shape.SetFriction(0);
			shape.SetFilter(NOT_GRABBABLE_FILTER);
			shape.ball = true;

			bodyCount++;

			finishedLoad = true;

			Schedule();

		}




		public override void Update(float dt)
		{
			base.Update(dt);

			if (finishedLoad)
				this.space.Step(dt);
